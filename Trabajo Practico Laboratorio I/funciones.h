#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED

/** \brief Realiza una suma de dos numeros flotantes y muestra el resultado
 *
 * \param x Es el primer operando ingresado
 * \param y Es el segundo operando ingresado
 *
 */
void suma (float,float);
/** \brief Calcula la resta de dos numeros flotantes y muestra el resultado
 *
 * \param x Es el primer operando ingresado
 * \param y Es el segundo operando ingresado
 *
 */
void resta (float,float);
/** \brief Realiza la division de dos numeros flotantes verificando que el segundo operando no sea 0 y muestra el resultado
 *
 * \param x Es el primer operando ingresado
 * \param y Es el segundo operando ingresado
 *
 */
void division(float,float);
/** \brief Hace la multiplicacion de dos numeros flotantes y muestra el resultado
 *
 * \param x Es el primer operando ingresado
 * \param y Es el segundo operando ingresado
 *
 */
void multiplicacion(float,float);

/** \brief Calcula el factorial del primer operando y verifica que no sea negativo ni flotante
 *
 * \param x Es el primer operando ingresado
 *
 */
void factorial (float);
#endif // FUNCIONES_H_INCLUDED
